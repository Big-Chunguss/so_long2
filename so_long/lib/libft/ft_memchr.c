#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*ps;
	size_t			i;

	i = 0;
	ps = (unsigned char *)s;
	while (n)
	{
		if (ps[i] == (unsigned char)c)
			return ((void *)(ps + i));
		i++;
		n--;
	}
	return (0);
}
